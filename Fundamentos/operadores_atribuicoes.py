# Atribuição Simples
x = 10
print(f"Atribuição Simples: x = {x}")

# Atribuição com Adição
x += 5  
print(f"Atribuição com Adição: x += 5 => x = {x}")

# Atribuição com Subtração
x -= 3  
print(f"Atribuição com Subtração: x -= 3 => x = {x}")

# Atribuição com Multiplicação
x *= 4  
print(f"Atribuição com Multiplicação: x *= 4 => x = {x}")

# Atribuição com Divisão
x /= 2  
print(f"Atribuição com Divisão: x /= 2 => x = {x}")

# Atribuição com Divisão Inteira
x //= 2  
print(f"Atribuição com Divisão Inteira: x //= 2 => x = {x}")

# Atribuição com Módulo
x %= 3  
print(f"Atribuição com Módulo: x %= 3 => x = {x}")

# Atribuição com Exponenciação
x **= 2  
print(f"Atribuição com Exponenciação: x **= 2 => x = {x}")

